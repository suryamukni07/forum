<b>SMKN 2 Pariaman</b><br>
SMKN 2 Pariaman merupakan salah sekolah menengah kejuruan yang terletak JL. SYAMRATULANGI NO. 11 PARIAMAN KAMPUNG BARU sekolah ini didirikan pada tahun 1984, dimana pada saat itu memiliki 3 buah jurusan yakninya Tata Niaga, Keuangan, Administrasi.<br />
	Namun perkembangan zaman dan teknologi SMKN 2 Pariaman membuka beberapa program studi baru yakninya : Pariwisata, Teknik Komunikasi dan Komputer, Perhotelan dan Tata Boga dimana membuat SMKN 2 Pariaman menjadi semakin mantap dalam mencerdaskan anak bangsa

