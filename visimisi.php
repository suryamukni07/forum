Visi dan Misi</h1>
<p>
<b>Visi :</b><br>
Berakhlak terpuji, berprestasi, kreatif, mandiri dan berwawasan lingkungan<br>
<b>Misi :</b><br>
1.	Membudayakan akhlak terpuji bagi seluruh warga sekolah.<br>
2.	Meningkatnya mutu pembelajaran untuk 8 Standar Nasional Pendidikan (SNP).<br>

<b>Tujuan :</b></br>
1.	Terwujudnya budaya akhlak terpuji warga sekolah sebagai budaya kenikmatan beribadah.<br>
2.	Terwujudnya lulusan dengan kompetensi sesuai Standar Nasional 100 %.<br>

